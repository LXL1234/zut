package cn.gpa.zut.domain;

public class Userteam {
	private String userteam_id;          
	private String   userteam_name;      
	private Integer   userteam_num;
	public String getUserteam_id() {
		return userteam_id;
	}
	public void setUserteam_id(String userteam_id) {
		this.userteam_id = userteam_id;
	}
	public String getUserteam_name() {
		return userteam_name;
	}
	public void setUserteam_name(String userteam_name) {
		this.userteam_name = userteam_name;
	}
	public Integer getUserteam_num() {
		return userteam_num;
	}
	public void setUserteam_num(Integer userteam_num) {
		this.userteam_num = userteam_num;
	}  

}
