package cn.gpa.zut.domain;

public class TeamRatio {
	private String teamratio_id;
	private Integer teamratio_onum;
	private String teamratio_rank;
	private Double teamratio_ratio;
	public String getTeamratio_id() {
		return teamratio_id;
	}
	public void setTeamratio_id(String teamratio_id) {
		this.teamratio_id = teamratio_id;
	}
	public Integer getTeamratio_onum() {
		return teamratio_onum;
	}
	public void setTeamratio_onum(Integer teamratio_onum) {
		this.teamratio_onum = teamratio_onum;
	}
	public String getTeamratio_rank() {
		return teamratio_rank;
	}
	public void setTeamratio_rank(String teamratio_rank) {
		this.teamratio_rank = teamratio_rank;
	}
	public Double getTeamratio_ratio() {
		return teamratio_ratio;
	}
	public void setTeamratio_ratio(Double teamratio_ratio) {
		this.teamratio_ratio = teamratio_ratio;
	}

}
